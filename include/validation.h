#pragma once
/*
 * validation.h — общие функции валидации пользовательского ввода.
 * Используются всеми .so плагинами.
 *
 * Все функции:
 *   - никогда не возвращают некорректное значение
 *   - при ошибочном вводе печатают причину и просят повторить
 *   - не падают при EOF, пустом вводе, нечисловом вводе
 */

#include <string>
#include <cstdint>
#include <vector>

// Ввод целого числа в диапазоне [lo, hi].
// При нечисловом вводе или выходе за диапазон — сообщение + повтор.
int      val_input_int(const std::string& prompt, int lo, int hi);

// Ввод непустой строки (произвольный текст).
// При пустой строке — сообщение + повтор.
std::string val_input_nonempty(const std::string& prompt);

// Ввод пути к существующему файлу.
// Проверяет: непустой, файл открывается на чтение.
// При ошибке — сообщение с причиной + повтор.
std::string val_input_existing_file(const std::string& prompt);

// Ввод пути для записи (новый или перезаписываемый файл).
// Проверяет: непустой, директория-родитель существует, файл открывается на запись.
// При ошибке — сообщение с причиной + повтор.
std::string val_input_writable_file(const std::string& prompt);

// Ввод необязательного пути (Enter = пропустить, возвращает пустую строку).
// Если введён непустой путь — проверяет, что файл открывается на запись.
std::string val_input_optional_file(const std::string& prompt);

// Ввод ключевого слова: непустое, только печатаемые символы.
std::string val_input_keyword(const std::string& prompt);

// Ввод hex-ключа длиной exactly key_len байт.
// Принимает строку hex-цифр без пробелов (длина = key_len * 2).
// При ошибке — объяснение + повтор.
std::vector<uint8_t> val_input_hex_key(const std::string& prompt, size_t key_len);

// Ввод двух байт ключа аффинного шифра: a (нечётное 1..255) и b (0..255).
// Возвращает {a, b}.
std::vector<uint8_t> val_input_affine_key();
